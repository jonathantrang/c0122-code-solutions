// declared variables that display a label and their values
var fullName = 'Jonathan Trang';
console.log('value of fullName: ', fullName);
var isCool = true;
console.log('value of isCool: ', isCool);
var totalPets = 1;
console.log('value of totalPets', totalPets);

// displays what kind of data type declared variables are
console.log('typeof fullName: ', typeof fullName);
console.log('typeof isCool: ', typeof isCool);
console.log('typeof totalPets: ', typeof totalPets);

// declares a variable to the value of null and explains what data type the variable is
var unknown = null;
console.log('value of unknown: ', unknown);
console.log('typeof unknown: ', typeof unknown);

// declares a variable without a value and explains what data type the variable is
var never;
console.log('value of never is: ', never);
console.log('typeof never: ', typeof never);
